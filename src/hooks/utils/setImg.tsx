export const setTitle = (events: number) => {

  if (events >= 1) {
    return 'AMATEUR'
  } else if (events >= 3) {
    return 'SEMI-PRO'
  } else if (events >= 10 ){
    return 'PRO'
  } else if (events >= 20) {
    return 'CHAMPION'
  }
}